package ex1;

/** Classe faisant des calculs basique **/
public class Calcul {


	/**
	 * Récupérer le resultat d'une addition
	 * @param a
	 * @param b
	 * @return
	 */
	public int addition(int a, int b){
		return a+b;
	}

	/**
	 * Récupérer la différence d'une soustraction
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
